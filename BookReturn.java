    // to fetch the issue book details  from the dtabase and display it to the panel
    public void getIssueBookDetails() {

        int bookId = Integer.parseInt(txt_bookId.getText());
        int studentId = Integer.parseInt(txt_studentId.getText());
        try {
            Connection con = DBConnection.getConnection();
            String sql = "select * from issue_book_details where book_id = ? and student_id = ? and status = ?";

            PreparedStatement st = con.prepareStatement(sql);
            st.setInt(1, bookId);
            st.setInt(2, studentId);
            st.setString(3, "pending");

            ResultSet rs = st.executeQuery();
            if (rs.next()) {

                lbl_issueId.setText(rs.getString("id"));
                lbl_bookName.setText(rs.getString("book_name"));
                lbl_studentName.setText(rs.getString("student_name"));
                lbl_checkoutDate.setText(rs.getString("checkout_date"));
                lbl_returnDate.setText(rs.getString("return_deadline_date"));
                lblBookError.setText(rs.getString(""));
            } else {
                lblBookError.setText("No Record Found");

                lbl_issueId.setText("");
                lbl_bookName.setText("");
                lbl_studentName.setText("");
                lbl_checkoutDate.setText("");
                 lbl_returnDate.setText("");

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
}
        
