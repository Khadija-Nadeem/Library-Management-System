    public boolean returnBook() {
        boolean isReturned = false;
        int bookId = Integer.parseInt(txt_bookId.getText());
        int studentId = Integer.parseInt(txt_studentId.getText());
        try {
            Connection con = DBConnection.getConnection();
            String sql = "update issue_book_details set status = ? where student_id = ? and book_id = ? and status = ?";
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1, "Returned");
            st.setInt(2, studentId);
            st.setInt(3, bookId);
            st.setString(4, "pending");
            int rowCount = st.executeUpdate();
            if (rowCount > 0) {
                isReturned = true;
            } else {
                isReturned = true;

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return isReturned;
    
    }
   
    //updating book count
    public void updateBookCount() {
        int bookId = Integer.parseInt(txt_bookId.getText());
        try {
            Connection con = DBConnection.getConnection();
            String sql = "Update book_details set quantity = quantity+1 where book_id = ?";
            PreparedStatement st = con.prepareStatement(sql);
            st.setInt(1, bookId);

            int rowCount = st.executeUpdate();
            if (rowCount > 0) {
                JOptionPane.showMessageDialog(this, "Book Count Updated");
                int initialCount = Integer.parseInt(lbl_returnDate.getText());
                lbl_returnDate.setText(Integer.toString(initialCount + 1));
            } else {
                JOptionPane.showMessageDialog(this, "Can't Book Count Updated");

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
   
